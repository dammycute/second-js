function sum(num1, num2, operator) {
    if(operator === '+'){
        console.log(num1 + num2)
    }else if(operator === '-'){
        console.log(num1 - num2)
    }else if(operator === '/'){
        console.log(num1 / num2)
    }else if(operator === '*' || 'x'){
        console.log(num1 * num2)
    }
}

sum(3, 5, "/")



